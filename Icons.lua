-- Metatable para permitir acesso case-insensitive e fallback
setmetatable(Icons, {
    __index = function(tbl, key)
        -- Tenta encontrar o ícone exato
        if rawget(tbl, key) then
            return rawget(tbl, key)
        end
        
        -- Tenta converter para lowercase
        local lowerKey = string.lower(tostring(key))
        if rawget(tbl, lowerKey) then
            return rawget(tbl, lowerKey)
        end
        
        -- Substitui underscores por hífens
        local dashedKey = string.gsub(lowerKey, "_", "-")
        if rawget(tbl, dashedKey) then
            return rawget(tbl, dashedKey)
        end
        
        -- Fallback: retorna ícone de interrogação
        warn("[Icons] Ícone não encontrado: " .. tostring(key))
        return "rbxassetid://10723406988" -- help-circle como fallback
    end
})

return Icons
